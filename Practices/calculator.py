#AP, 1st Period, Calculator

first_number = float(input("Please enter a number, as a digit not a word: "))
second_number = float(input("Please enter another number, as a digit not a word: "))

print(f"{first_number:.2f} + {second_number:.2f} = {first_number + second_number:.2f}")
print(f"{first_number:.2f} - {second_number:.2f} = {first_number - second_number:.2f}")
print(f"{first_number:.2f} x {second_number:.2f} = {first_number * second_number:.2f}")
print(f"{first_number:.2f} / {second_number:.2f} = {first_number / second_number:.2f}")
print(f"{first_number:.2f} % {second_number:.2f} = {first_number % second_number:.2f}")
print(f"{first_number:.2f} // {second_number:.2f} = {first_number // second_number:.2f}")
print(f"{first_number:.2f} ^ {second_number:.2f} = {first_number ** second_number:.2f}")